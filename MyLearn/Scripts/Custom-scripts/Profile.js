function Profile(UserName, Name, Email, PhoneNumber,Address,Country,University,Status){
  /*UserName: is the name of the user*/
  this.UserName=UserName;
  /*Name: is the name of the student or teacher*/
  this.Name=Name;
  /*Email: Email of user*/
  this.Email=Email;
  /*PhoneNumber: The phone number of the user*/
  this.PhoneNumber=PhoneNumber;
  /*Address: Exact address of user*/
  this.Address=Address;
  /*Country:The Country of the user*/
  this.Country=Country;
  /*The University of the user*/
  this.University=University;
  /*Status:Status of user, eneabled or disabled*/
  this.Status=Status;
}





